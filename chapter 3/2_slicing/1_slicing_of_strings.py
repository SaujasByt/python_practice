# a string in python can be sliced for getting a part of the trings
# with the index starting from 0 or -1(from last)
# for eg:-
name="saujas"
nameshort=name[0:3]# it will start from index 0 to 3(excluding 3)
print(nameshort)

char=name[1:] #is same as [1:max] ofc excluding 1
print(char)

char2=name[:4]
print(char2) #same as [0:4]
